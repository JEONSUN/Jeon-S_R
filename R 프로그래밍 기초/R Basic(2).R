# 벡터에 데이터 추가 및 벡터들의 결합
# 기본적으로 벡터 뒤에 결합
x <- c(11,12,13,14)
c(x,15)
y <- c(16,17,18)
c(x,y)

# 함수 append : 추가되는 스칼라나 벡터의 위치 조절 가능

append(x,15)
append(x,15,after = 2)
append(x,y)
append(x,y,after = 3)

# 일정한 구조를 갖는 벡터의 생성
# 콜론 연산자 
# 1씩 감소나 증가일때만 사용
1:5
-3:3
1.5:5.4
5:0

# 함수 seq()에 의한 수열 생성
# 일정 간격으로 만들고 싶을때 사용

seq(from = 0, to = 5) # 0부터 5까지
seq(from = 0, to = 5, by = 2) # 2씩 증가 0~5까지
seq(from = 0 , to = 5, length = 3) # 간격이 같은 3개의 숫자
seq(from = 0 , by = 2, length = 3)  # 0부터 간격이 2인 3개의 숫자

# 한 숫자만 입력된 경우 : 1을 시작점, 1씩 증가(감소), 지정된 숫자를끝점
seq(3)
seq(-3)

# rep()에 의한 반복된 패턴이 있는 데이터 생성
# times의 활용

rep(1, times = 3)
rep(1:3, times = 2)
rep(c('M','F'),times = c(2,3))

# times에 하나의 숫자 지정 : 데이터 전체를 숫자만큼 반복
# times에 벡터 지정 : 반복 대상 데이터와 일대일 대응

# each의 사용
rep (1:3, each = 2) # 데이터 각 요소가 each번 반복

# 옵션 each와 times의 활용
rep(1:3, each = 2, times = 2) # 데이터 각 요소가 each번 반복, 전체를 times번 반복

# 옵션 each와 length의 활용
rep(1:3, each = 2, length = 8) # 각 요소가 each번 반복되는 과정을 길이가 length가 될 때까지 반복복

