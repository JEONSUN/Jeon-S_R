# 벡터
# 유형 : 숫자형(numeric) - 정수형(integer)과 실수형(double)으로 나눠짐, 문자형(character), 논리형(logical)
# 벡터의 기본 특성
# 벡터의 생성 : 함수 c()
x <- c(TRUE, FALSE, TRUE) 
y1 <- c(1L,2L,3L)  
y2 <- c(1.1,3.5,10.4)
z <- c('one','two','three')

# 벡터의 구성요소 : 모두 같은 유형의 데이터
typeof(x)
typeof(y1)
typeof(y2)
typeof(z)

# 벡터의 길이 : 벡터를 구성하고 있는 요소 개수
y1 <- c(1,3,5)
length(y1)

# 스칼라(길이가 1인 벡터)의 생성
# c() 함수를 사용하지 않아도 됨.
a <- 1;a

# 다른 유형의 데이터가 뒤섞여 있는 경우
# 문자형 데이터가 하나라도 포함되면 문자형 벡터가 됨 : 문자형이 가장 복잡한 형태
c(1,'1',TRUE)

# 숫자형과 논리형이 함께 있으면 숫자형 벡터가 됨 : 논리형이 가장 단순한 형태
c(3,TRUE,FALSE)

# 벡터의 구성 요소에 이름 붙이기
# 처음 입력할 때
c(seoul = 9930, busan = 3497,inchon = 2944, suwon = 1194)

# 이미 생성된 벡터

pop <- c(9930,3497,2944,1194)
names(pop) <- c('seoul','busan','inchon','suwon') # pop 벡터에 names로 이름을 붙여줌
pop

# scan에 의한 벡터 생성 : 외부 파일의 입력 및 자판에서 직접 데이터 객체 생성이 가능한 함수
# 숫자형 벡터
x <- scan()
12
